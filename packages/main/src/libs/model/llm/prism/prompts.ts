import type { Facet, FacetCritique } from "./schemas.js";

/**
 * 除"棱面拆分"外，全部提示词只表达"要什么语义"，产物就是自然语言本身，
 * 代码不解析其内容。代码可见的约定只有两个哨兵标记：
 * - PASS_TOKEN：批判者认为本视角无问题时只输出它，代码据此在精炼前滤掉该视角；
 * - KEEP_TOKEN：精炼器认为无需修改时只输出它，代码据此停机回退。
 *
 * 复杂度均衡原则：批判者（单视角、有余力）输出完整评审意见（评价、问题、原因、建议），
 * 并为每条问题附上"原文片段 → 改写后片段"的修改示范作为参考——把"翻译成具体文字"
 * 的重活前移；精炼器以意见为依据、以示范为起点做合并与仲裁，不从零创作。
 * system 固定写死角色与规则,user 只注入参数。绝不让 LLM 生成提示词本身。
 */

/** 批判者"本视角无问题"的哨兵标记；出现即视为该视角通过，不进精炼。 */
export const PASS_TOKEN = "[[PASS]]";

/** 精炼器"无需修改"的哨兵标记；出现即视为保留当前稿。 */
export const KEEP_TOKEN = "[[KEEP]]";

// ── S1 · 棱面拆分（全流程唯一一次结构化提取）─────────────────────
export const SPLIT_SYSTEM = `你是评估维度设计师。给定一个问题和它的一份初稿答案，先想清楚：要判断这份答案到底好不好，最关键、且彼此不重叠的角度有哪些。

找出 2 到 5 个这样的正交视角，合起来能覆盖对本题答案的主要判断。为每个视角想一个便于指代的简短名字，说清它具体检查答案的哪一方面，以及为什么它对这道题是关键的、和别的视角不重叠。

宁少勿滥：可有可无的、或者和已经想到的视角重叠的角度，就不要列。视角要贴着这个具体问题来，不要套用放之四海皆准的通用清单。这一步只负责找评估角度，不要去评价答案本身好坏。`;

export function splitUser(query: string, draft: string): string {
    return `问题：
"""
${query}
"""

初稿答案：
"""
${draft}
"""

请找出评估这份答案好坏最关键的几个正交视角。`;
}

// ── S2 · 分面批判（参数化单模板，所有棱面共用；评审意见 + 附修改示范）──
export function critiqueSystem(facet: Facet): string {
    return `你是"${facet.name}"这一个视角的专职评审。只从这一个视角审查答案，不要越界去评论其它视角的问题。

审查时，始终对照用户真正想要的是什么，不要脱离原始问题空谈。用简洁的自然语言写一份评审意见：单从这个视角看，这份答案整体做得怎么样、存在哪些具体而且能定位到的问题、每个问题为什么是问题、应该朝什么方向改。你的意见会原文转交给精炼者，理由要写清楚——精炼者靠你的理由来权衡取舍。

在每条问题的意见之后，再附上一段修改示范作为参考：原样摘录答案中有问题的那个片段，然后写出这个片段改写后的样子。示范要达到成品质量，让精炼者可以直接参考落笔；但示范只是对你意见的具体演示，不能取代意见本身。示范只覆盖本视角有问题的部分，不要顺手重写没问题的内容。

如果从这个视角看确实没有问题，就不要为了凑内容硬找毛病。此时只输出下面这一行标记，除此之外什么都不要输出：
${PASS_TOKEN}`;
}

export function critiqueUser(query: string, draft: string, facet: Facet): string {
    return `原始问题：
"""
${query}
"""

你这个视角要检查的是：${facet.checks_what}

待审查的答案：
"""
${draft}
"""`;
}

// ── S3 · 合并精炼（依据意见仲裁，以示范为起点落笔，不从零创作）──────
export const REFINE_SYSTEM = `你是精炼器。你会拿到原始问题、一份当前答案、以及多个视角的评审意见——每条意见都说明了问题所在和理由，并附有"原文片段 → 改写后片段"的修改示范。

你的工作是依据这些意见修改当前答案，以示范为起点落笔，而不是从零创作：
- 逐条处理意见：示范可用就直接落到对应位置；示范与上下文衔接不顺、或与其它视角的修改有交叠时，参照该条意见的方向和理由做调整，不要偏离意见本身的意图。
- 不同视角的意见互相冲突时，对照各自给出的理由，采纳更贴合原始问题要求的那条，放弃另一条。
- 某条意见或示范违背原始问题要求的（加入用户没要求的内容、或丢掉用户要求的内容），直接放弃这条。
- 意见没有触及的部分，保持原文一字不动；保持原有结构和排版，除非意见明确指出结构或格式本身有问题。

直接输出修改后的完整答案——只输出答案本身，不要任何前言、解释、评论或改动说明，因为你的输出会原样交付给用户。

如果所有意见都不值得采纳、任何改动都只会让答案变差，那就不要改写。此时只输出下面这一行标记，除此之外什么都不要输出：
${KEEP_TOKEN}`;

export function refineUser(
    query: string,
    current: string,
    critiques: FacetCritique[]
): string {
    const critiqueBlock = critiques
        .map((c) => `【视角：${c.facet}】\n${c.text}`)
        .join("\n\n---\n\n");

    return `原始问题：
"""
${query}
"""

当前答案：
"""
${current}
"""

各视角的评审意见（附修改示范）：
${critiqueBlock}`;
}