export const anchorRefineUser = (p: {
    body: string;
    draft: string;
    critiques: { facet: string; text: string }[];
}): string =>
    `用户原话:\n"""${p.body}"""\n\n当前整理稿:\n"""${p.draft}"""\n\n各棱面评审:\n${p.critiques
        .map((c) => `【${c.facet}】${c.text}`)
        .join('\n\n')}\n\n请输出修订后的完整五节稿。`;

/* ==================== EXTRACT:唯一的 JSON 提取 ==================== */

const TermSchema = z.object({
    name: z.string().describe('整理稿该项「名称:」后的内容,原样搬运,不做任何规范化'),
    intent: z.string().describe('整理稿该项「说明:」后的内容,原样搬运'),
});

export type TermType = z.infer<typeof TermSchema>;

export const AnchorSchema = z.object({
    inputs: z.array(TermSchema).describe('「输入」节逐项:名称→name,说明→intent'),
    outputs: z.array(TermSchema).describe('「输出」节逐项:名称→name,说明→intent'),
    goal: z.string().describe('「目标」节的一句话,原样提取;整理稿中为空则空字符串'),
    criteria: z.array(z.string()).describe('「验收标准」节逐条;(无)则空数组'),
    negative: z.array(z.string()).describe('「禁用」节逐条;(无)则空数组'),
});

export type AnchorOut = z.infer<typeof AnchorSchema>;

export const ANCHOR_EXTRACT_SYSTEM = `你是结构提取器。把"五节整理稿"忠实转换为 JSON。只做格式转换:「名称:」映射到 name,「说明:」映射到 intent,不增删信息、不改写措辞、不做任何再判断。若某项缺少"说明",intent 置为空字符串,不许自行补写。`;

export const anchorExtractUser = (draft: string): string =>
    `整理稿:\n"""${draft}"""\n\n请转换为 JSON。`;